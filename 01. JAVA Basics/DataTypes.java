public class DataTypes {
    public static void main(String[] args) {
        System.out.println("Hello");

        // comments
        /* Multilines comment */

        // Data Type

        // Byte (-128 to 127)
        byte num = 100;
        System.out.println(num);

        // short 16 bits Range (-32,768 to 32,767)
        short num1 = 12345;
        System.out.println(num1);

        // int 32 bits Range (-2^31 to 2^31-1)
        int num2 = 1234567898;
        System.out.println(num2);

        // long 64 bits Range (-2^63 to 2^63-1)
        long num3 = 1234567899;
        System.out.println(num3);

        // float: 4 bytes / 32 bits, decimals (needs 'f' suffix, e.g., 3.14f) (6-7
        // digits of decimal digit precision)
        float num4 = 5.1234567f;
        System.out.println(num4);

        // double: 8 bytes / 64 bits, decimals (Most common for fractions)
        double num5 = 1.123456789;
        System.out.println(num5);

        // char: 2 bytes / 16 bits, single character (e.g., 'A')
        char ch = 'x';
        System.out.println(ch);

        // boolean: 1 bit, true or false
        boolean fl = true;
        System.out.println(fl);


        // 
    }
}